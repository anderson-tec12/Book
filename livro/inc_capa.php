    <p><strong>Há muitos séculos nossa região</strong>, a Cuesta basáltica, tem sido ocupada por populações humanas.
      Mais antiga ainda é sua história geológica.
      O <em>Atlas da Cuesta</em> foi produzido pelo Instituto Itapoty, organização não governamental da região,
      com o apoio financeiro do Fundo de Defesa dos Direitos Difusos, do Ministério da Justiça brasileiro,
      e a ajuda de muitos profissionais. é um presente, feito com dedicação e emoção, para <span class="caps">você!</span>
      Pois nossa história e a história ambiental desta região são muito íntimas.</p>

    <p>Este atlas enaltece o lugar em que vivemos e as pessoas que acreditam no valor da educação.
      Bota muita fé em você, jovem <em>Homo sapiens</em> ciente de que a natureza merece respeito, em vez de ser maltratada.</p>

    <p>Descobrimos, nesses dois anos de trabalho no Atlas, que, mais que a formação geológica da Cuesta,
      existe também uma história humana, caracterizada pela vida rural, pelas festas tradicionais de fé religiosa,
      pela cultura musical, pela culinária e pelos belos atrativos naturais. E que toda essa história teve consequências as mais diversas,
      agora relatadas neste livro. Você verá que existem meios para cada um de nós atuar transformando a realidade para melhor.</p>

    <p>O conteúdo mescla textos e informações com fotografias e ilustrações,
      um gostoso e elaborado “aperitivo” intelectual e visual para despertar seu interesse pelos temas e sua vontade de aprender,
      ensinar, fazer mais amizades e cooperar. Se alguma palavra ou expressão aparecer deste jeito,
      vá até o glossário e descubra o que ela significa.</p>

    <p>Um mundo melhor depende de pessoas melhores, e nós podemos ser muito melhores do queimaginamos.
      Juntos, estamos dourando as sementes que irão verdejar novas esperanças, mais cores e boa música nas mentes empoeiradas, nos pálidos corações e nos jardins abandonados...
      “Antes de sair com a nobre missão de mudar o mundo, dê duas voltas dentro de sua própria casa”, nos alerta um antigo provérbio.</p>

    <p>Seja bem-vindo a um universo de descobertas sobre a sua própria história, a história dos humanos que, por séculos e séculos, interagem com esta paisagem criada há milhões de anos.</p>
    <p class="signature"><span class="caps">Boa leitura!</span> <br>
      Equipe Itapoty</p>
