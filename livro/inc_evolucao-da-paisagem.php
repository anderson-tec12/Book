    <p>Você imagina como era a natureza na região da Cuesta muito tempo atrás? Por exemplo, na “Era do Gelo”?</p>
    <p>Foi mais ou menos nesse período, na época do Pleistoceno (entre 1 milhão e 800 mil anos atrás),
      que a natureza aqui começou a se formar como a conhecemos hoje. O clima, que era úmido tornou-se muito seco,
      e a vegetação teve de acompanhar essa mudança, tornando-se uma espécie de mistura de Caatinga e Cerrado.
      Nas áreas mais altas da Cuesta, de clima mais frio, é bem possível que existisse a Floresta de Araucária.
      E uma vegetação mais exuberante, parecida com a Mata Atlântica, conseguiu sobreviver nos poucos lugares mais úmidos e com solo mais fértil. Depois,
      na época do Holoceno (de 18 mil anos atrás até hoje), o clima começou a ficar mais quente e úmido.
      Como a Mata Atlântica é adaptada a essas condições, ela se espalhou entre as áreas de Cerrado que continuaram presentes.
      Da Caatinga sobraram apenas algumas espécies típicas, como o Mandacaru.
      E a Floresta de Araucária? Não temos certeza, mas algumas fazendas da região possuem araucárias bem antigas,
      que podem ser descendentes daquelas, da época do Pleistoceno.</p>


